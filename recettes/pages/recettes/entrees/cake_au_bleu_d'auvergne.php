		<h3>Cake au bleu d'Auvergne</h3>

		<p>Pour 8 personnes :</p>

		<p>Préparation : 15 min / Cuisson : 45 min</p>

		<ul>

			<li>3 oeufs</li>

			<li>150 g de farine</li>

			<li>1 sachet de levure</li>

			<li>10 cL d'huile de tournesol</li>

			<li>12,5 cL de lait entier</li>

			<li>100 g de gruyère râpé</li>

			<li>150 g de bleu d'auvergne</li>

			<li>50 g de figues sèches</li>

			<li>30 g de noix concassées</li>

			<li>5 cL d'Armagnac (facultatif)</li>

			<li>sel/poivre</li>

		</ul>

		<p>Péchauffer le four à 180°C (Th.5/6). Couper les figues en morceaux et les faire tremper dans l'Armagnac.</p>

		<p>Mélanger au fouet les oeufs, la farine, la levure, le sel et le poivre. Incorporer petit à petit l'huile et le lait préalablement tiédi. Ajouter le gruyère. Emietter le bleu et l'incorporer à la pâte. Ajouter les noix et les figues. Mélanger le tout. Verser la préparation dans le moule à cake et mettre au four pendant 45 min. Démouler et servir accompagné d'une salade verte. Il peut également se servir à l'apéritif ou bien remplacer le pain sur un plateau de fromages.</p>

		<p>Ce cake peut se préparer 24 h à l'avance. Le couper en tranches et les faire réchauffer 1 min sous le grill du four. Le cake peut être également réalisé dans les moules Proflex : mini-cakes et petits fours.</p>



