		<h3>Pâté Végétarien</h3>

		<ul>

			<li>5 échalotes</li>

			<li>2 gousses d'ail</li>

			<li>1 œuf</li>

			<li>50 g de beurre</li>

			<li>150 g de biscottes</li>

			<li>1 cuillère à soupe de persil haché</li>

			<li>1 cuillère à café de thym</li>

			<li>Noix de muscade</li>

			<li>3 cL d'eau ou bouillon</li>

		</ul>



		<p>Couper les échalotes très fin, écraser l’ail.</p>



		<p>Verser le tout dans une casserole avec le beurre, les biscottes écrasées, le thym, la muscade et le bouillon.</p>



		<p>Cuire 15 à 20 minutes</p>



		<p>Mettre dans un moule à cake passé sous l’eau froide.</p>



		<p>Manger froid.</p>





