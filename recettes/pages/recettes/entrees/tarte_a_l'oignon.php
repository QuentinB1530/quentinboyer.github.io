		<h3>Tarte à l'Oignon</h3>

		<ul>

			<li>100 g de beurre</li>

			<li>6 à 8 oignons moyens</li>

			<li>60 g de lard</li>

			<li>3 œufs</li>

			<li>1 verre de crème fraîche</li>

			<li>Sel</li>

			<li>Poivre</li>

			<li>Un fond de pâte brisée</li>

		</ul>



		<p>Emincer les oignons. Les faire revenir dans le beurre; ils doivent être légèrement dorés. Quand ils sont refroidis, incorporer les œufs battus, le sel, la crème, le lard coupés en dés. Foncer le moule à tarte avec la pâte brisée; piquer la pâte avec une fourchette pour éviter les boursouflures. Remplir avec la masse aux oignons. Faire cuire à four chaud pendant 20 à 25 minutes.</p>





