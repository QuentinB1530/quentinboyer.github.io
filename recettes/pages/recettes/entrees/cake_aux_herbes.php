		<h3>Cake aux Herbes</h3>

		<ul>

			<li>200 g de farine</li>

			<li>1 sachet de levure</li>

			<li>6 cuillère à soupe d'huile</li>

			<li>3 œufs</li>

			<li>10 cL de lait</li>

			<li>2 tranches de jambon blanc et 1 tranche de jambon cru</li>

			<li>1 cuillère à soupe de persil</li>

			<li>1 cuillère à soupe de ciboulette</li>

			<li>1 cuillère à soupe de menthe</li>

			<li>1 cuillère à soupe de coriandre</li>

			<li>1 cuillère à soupe de cerfeuil</li>

			<li>1 cuillère à soupe d'estragon</li>

			<li>Sel et poivre</li>

		</ul>



		<p>Farine, levure, œufs, huile, ½ cuillère à café de sel, poivre.</p>



		<p>Faire tourner 3 minutes.</p>



		<p>Verser le lait chaud. Mixer encore 1 minutes.</p>



		<p>Ajouter jambon en dés et les herbes.</p>



		<p>Verser dans le moule beurré et fariné.</p>



		<p>Laisser reposer 15 min</p>



		<p>Th. 7 / 1 heure</p>





