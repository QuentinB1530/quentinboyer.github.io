		<h3>Clafoutis de Tomates cerises au Comté</h3>

		<p>Pour 4 personnes :</p>



		<ul>

			<li>40 tomates cerises</li>

			<li>25 cL de crème entière</li>

			<li>25 cL de lait</li>

			<li>4 oeufs</li>

			<li>4 cuillères à café de Maïzena</li>

			<li>150 g de Comté</li>

			<li>ciboulette, sel et poivre</li>

		</ul>



		<p>Astuce : Remplacer le Comté par du fromage de brebis basque pour un goût différent</p>



		<p>Laver et essuyer les tomates, couper le comté en dés, puis les répartir dans 4 ramequins bien beurrés.</p>



		<p>Dans un bol, battre les oeufs avec la Maïzena, puis délayer avec la crème entière et le lait. Saler et poivrer. Verser cette préparation dans les ramequins, avant d'enfourner.</p>



		<p>A la sortie du four, parsemer les clafoutis de ciboulette.</p>



		<p>Cuisson : Au four - Th. 6 (180°C) - 15 à 20 minutes</p>





