		<h3>Saint-Jacques de Noël</h3>

		<p>Pour : 4 couverts / Préparation : 30 min / Cuisson : 5 min</p>



		<ul>

			<li>12 coquilles Saint Jacques</li>

			<li>1 filet d'huile d'olive</li>

			<li>2 citrons jaunes</li>

			<li>3 branches de persil</li>

			<li>2 tranches de pain de mie toastées</li>

			<li>20 g de petits câpres</li>

			<li>Sel et poivre du moulin</li>

		</ul>



		<ol>

			<li>La veille, chez son poissonnier, faire préparer les Saint-Jacques. Garder les coquilles, les noix et les bardes (cette petite membrane qui entoure la noix).</li>

			<li>Faire tremper les bardes dans de l'eau toute une journée.</li>

		</ol>



		<ul>

			<li>1 échalotte</li>

			<li>1 cuillère à soupe d'huile d'olive</li>

			<li>70 g de beurre</li>

			<li>1 brindille de thym</li>

			<li>25 cL de vin blanc</li>

			<li>Poivre du moulin</li>

			<li>1 gousse d'ail</li>

		</ul>



		<ol>

			<li>Emincer léchalotte.</li>

			<li>Dans une poêle chaude, avec un filet d'huile d'olive, faire suer les bardes de Saint-Jacques préalablement. Les égoutter afin qu'elles perdent leur humidité, puis les égoutter dans une passette.</li>

			<li>Faire chauffer une cocotte avec de l'huile d'olive et 20 g de beurre. Saisir à nouveau les bardes en les colorant bien, puis ajouter l'échalote, l'ail écrasé et la brindille de thym.</li>

			<li>Déglacer avec le vin blanc et laisser réduire de moitié. Filtrer le et laisser au frais.</li>

			<li>Peler les citrons (enlever la peau blanche). Récupérer les segments et les couper dans le sens de la longueur. Conserver au frais.</li>

			<li>Presser ce qu'il reste du citron. Laver et émincer le persil plat.</li>

			<li>Toaster en petits cubes et égoutter les petites câpres.</li>

			<li>Préchauffer le four à 180°C (Th. 6).</li>

			<li>Faire fondre 50 g de beurre dans une casserole jusqu'à ce qu'elle prenne une couleur noisette. Conserver au frais.</li>

			<li>Assaisonner les noix avec un filet d'huile d'olive, un peu de sel et un tour de poivre. Enfourner les coquilles propres dans lesquelles sont déposé les noix pendant 5 minutes.</li>

			<li>Faire chauffer la sauce et la monter à l'aide d'un fouet avec un beurre noisette durci et couper en petits morceaux.</li>

			<li>Rectifier l'assaisonnement, puis verser sur les coquilles cuites et déposer les condiments.</li>

			<li>Servir chaud.</li>

		</ol>





