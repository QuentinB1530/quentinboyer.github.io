		<h3>Cake Salé</h3>

		<ul>

			<li>1 verre de vin blanc</li>

			<li>1 verre d’huile</li>

			<li>4 œufs</li>

			<li>250 g de farine</li>

			<li>100 g de gruyère râpé</li>

			<li>150 g de jambon fumé coupé en morceaux</li>

			<li>1 boite de champignon coupés (boite moyenne)</li>

			<li>1 paquet et demi de levure alsacienne</li>

			<li>1 pincée de sel</li>

		</ul>



		<p>Mélanger le vin, l’huile, et les œufs un à un. Ajouter le gruyère, la farine, la levure et le sel. Terminer par le jambon et les champignons.</p>



		<p>Cuire dans un moule à cake ou en couronne beurré et fariné.</p>



		<p>45 min / 170° à 180°C</p>



		<p>Four moyen</p>



		<p>Démoulé chaud</p>



