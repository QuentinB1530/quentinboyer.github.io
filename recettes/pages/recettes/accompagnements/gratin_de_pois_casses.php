		<h3>Gratin de Pois cassés</h3>

		<ul>

			<li>300 g de pois cassés</li>

			<li>150 g de beurres</li>

			<li>100 g de gruyère râpé</li>

			<li>1 oignon</li>

			<li>3 cL de crème fraîche</li>

			<li>1 carotte</li>

		</ul>

		<p>Cuire les pois cassés avec l’oignon et la carotte. Mixer les pois cassés en purée. Ajouter crème fraîche et beurre. Mélanger. Mettre dans un plat et parsemer de gruyère râpé. Gratiner à four très chaud.</p>



