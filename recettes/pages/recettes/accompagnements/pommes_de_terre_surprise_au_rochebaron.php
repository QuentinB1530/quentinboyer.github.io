		<h3>Pommes de terre surprise au Rochebaron</h3>

		<ul>

			<li>4 grosses pommes de terre (type Monalisa)</li>

			<li>4 tranches de jambon de Bayonne</li>

			<li>250 g de Rochebaron</li>

			<li>4 cuillère à soupe de crème fraîche</li>

			<li>4 cuillère à soupe de ciboulette</li>

			<li>Sel</li>

			<li>Poivre</li>

		</ul>

		<p>Lavez les pommes de terre et faites-les cuire dans de l’eau bouillante salée 25 minutes.</p>

		<p>Egouttez et laissez refroidir.</p>

		<p>Coupez les pommes de terre en deux dans le sens de la longueur.</p>

		<p>Evidez une partie de la chair, écrasez celle-ci. Ajoutez-y la crème fraîche et la ciboulette. Mélangez la préparation et assaisonnez.</p>

		<p>Préchauffez le four à 190°C (Th.6) pendant 10 minutes.</p>

		<p>Fourrez les pommes de terre à l’aide de la préparation. Placer dessus une tranche de jambon de Bayonne puis une tranche de Rochebaron.</p>

		<p>	Disposez-les ensuite dans un plat à gratin. Faites-les cuire 10 minutes jusqu’à ce que le fromage soit bien doré.</p>

		<p>Avec une salade de mesclun ou une frisée.</p>



