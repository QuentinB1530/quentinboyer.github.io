		<h3>Pommes de terre à la Paysanne</h3>

		<ul>

			<li>1 kg de pommes de terre</li>

			<li>2 épaisses tranches de jambon cuit</li>

			<li>2 oignons</li>

			<li>1 cuillère à soupe de moutarde forte</li>

			<li>15 cL de bouillon de poulet fait avec ½ cube</li>

			<li>10 cL de crème fraîche</li>

			<li>50 g de gruyère râpé</li>

			<li>25 g de beurre</li>

			<li>Sel, poivre et persil haché</li>

		</ul>

		<p>Emincer les oignons. Couper le jambon en petits dés, faire fondre le beurre.</p>

		<p>Faire fondre le beurre.</p>

		<p>Faire revenir les oignons 5 minutes (transparents).</p>

		<p>Peler les pommes de terre, faire des fines rondelles.</p>

		<p>Etaler la moitié dans un plat beurré. Sel, poivre.</p>

		<p>Recouvrir avec la moitié des oignons et répartir les dés de jambon par-dessus.</p>

		<p>Badigeonner les dés de jambon avec la moutarde.</p>

		<p>Recouvrir avec le reste d’oignons puis des pommes de terre.</p>

		<p>Sel, poivre. Verser le bouillon.</p>

		<p>Cuire à four chaud / 190°C / Th. 6 / 40 min</p>

		<p>Répartir la crème puis le gruyère râpé. Poursuivre la cuisson 15 – 20 min.</p>

		<p>Décoré de Persil haché.</p>



