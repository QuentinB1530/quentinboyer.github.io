		<h3>Sorbet à l'Orange</h3>

		<ul>

			<li>200 g de sucre</li>

			<li>300ml d'eau</li>

			<li>400 ml de jus d'orange</li>

		</ul>



		<ol>

			<li>Méangez l'eau et le sucre dans une casserole et portez à ébullition.</li>

			<li>Laissez le sirop refroidir à température ambiante.</li>

			<li>Versez le jus d'orange en remuant et placez la préparation dans le réfrigérateur pour qu'elle refroidisse (minimum 12 heures).</li>

		</ol>



		<p>Variantes :</p>



		<ul>

			<li>On peut utiliser du jus de citron ou de pamplemousse ou des kiwis en purée (dans ce cas, ajoutez une cuillère à soupe de jus de citron).</li>

