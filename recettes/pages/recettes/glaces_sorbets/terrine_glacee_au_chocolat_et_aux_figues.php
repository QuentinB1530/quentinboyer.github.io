		<h3>Terrine glacée au Chocolat et aux Figues</h3>

		<p>Préparation : 25 min</p>



		<p>Pour 8 personnes :</p>



		<ul>

			<li>2 cuillères à soupe de café soluble</li>

			<li>17,5 cL d'eau bouillante</li>

			<li>175 g de figues séchées</li>

			<li>60 cL de crème fraîche épaisse</li>

			<li>5 cuillères à soupe de Tia Maria ou autre liqueur parfumée au café</li>

			<li>300 g de chocolat noir cassé en morceaux</li>

			<li>50 g de beurre dou</li>

			<li>feuilles ou spirales de chocolat pour décorer</li>

		</ul>



		<p>Diluer le café dans l'eau et y mettre les figues à tremper pendant 24 heures. Tapisser de film étirable un moule à cake de 900 g de contenance de préférence à bords droits. Fouetter 35 cL de crème fraîche avec 2 cuillères à soupe de liqueur de café jusqu'à ce qu'elle prenne. Gainer en tout l'intérieur du moule. Laisser raffermir plusieurs heures au congélateur. Egoutter les figues en réservant le liquide au café. Faire fondre au bain-marie le chocolat et le beurre, avec le liquide. Hors du feu ajouter le reste de liqueur. Remuer légèrement pour obtenir une sauce onctueuse.</p>



		<p>Fouetter le reste de crème fraîche jusqu'à ce qu'elle prenne, y incorporer la sauce au chocolat et les figues et remuer délicatement jusqu'à ce que les ingrédients soient bien mélangés. Verser la préparation dans le moule tapissé de crème et lisser à la surface. Mettre à raffermir au congélateur plusieurs heures ou toute une nuit. Transférer au réfrigérateur environ 1h. Démouler la terrine et décoller le film étirable. Décorer généreusement de feuilles de chocolat ou de spirales et servir coupé en tranches.</p>



