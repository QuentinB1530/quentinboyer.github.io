		<h3>Glace aux deux Pralines et Fruits rouges</h3>

		<p>Préparation : 30 min / Cuisson : 10 min</p>



		<p>Pour 4 personnes :</p>



		<ul>

			<li>50cl de Crème liquide</li>

			<li>250g de Sucre glace</li>

			<li>120g de Pralines brunes</li>

			<li>120g de Pralines roses</li>

			<li>250g de Fruits rouges (frais ou surgelés)</li>

		</ul>



		<p>Concasser séparément les pralines brunes et les pralines roses. Fouetter la crème très froide en ajoutant le sucre glace au fur et à mesure que la crème monte. Diviser cette crème en deux. Dans l'une, incorporer les pralines brunes ; dans l'autre, les pralines roses. Versez une couche de la préparation aux pralines brunes dans un moule à cake tapissé d'un film étirable. Réserver 30 min au congélateur. Mixer les fruits rouges. Incorporer ce coulis à la préparation aux pralines roses. Verser dans le moule et faire à nouveau durcir au congélateur. Recouvrer du reste de la préparation. Réserver 5 h au congélateur. Démouler au moment de servir.</p>





