		<h3>Glace à la Noix de coco</h3>

		<ul>

			<li>250 ml de lait entier</li>

			<li>150 g de sucre</li>

			<li>2 œufs</li>

			<li>450 g de crème liquide</li>

			<li>40 g de noix de coco séchée</li>

			<li>1/2 cuillère à café d'extrait de vanille.</li>

		</ul>



		<ol>

			<li>Mélangez le lait, l'œuf, le sucre dans une petite casserole.</li>

			<li>Faites chauffer le mélange à feu doux, sans bouillir, tout en remuant jusqu'à ce qu'il commence à épaissir et prenne la consistance d'une crème anglaise légère.</li>

			<li>Laissez refroidir à température ambiante et ajoutez la noix de coco, la crème et la vanille tout en remuant.</li>

			<li>Mettez au réfrigérateur (minimum 12 heures).</li>

		</ol>

