		<h3>Glace à la Fraise</h3>

		<p>Préparation : 10 min</p>



		<ul>

			<li>500 g de fraises</li>

			<li>250 g de sucre en poudre</li>

			<li>250 ml de crème fraîche épaisse</li>

			<li>1 citron</li>

		</ul>

		<p>Laver et équetter les fraises. Les couper en deux. Presser le jus des citrons. Dans le bol d'un mixer, mettre les fraises, le sucre en poudre, la crème fraîche et le jus de citron. Mixer pendant 2 à 3 min pour que le mélange soit bien onctueux. Verser la préparation dans les moules flexibles. Placer au congélateur pendant 8 heures. Servir décoré de fraises et de quelques feuilles de menthe.</p>

