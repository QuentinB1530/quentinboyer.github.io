		<h3>Glace au Chocolat</h3>

		<ul>

			<li>250 mL de lait entier</li>

			<li>100 g de sucre</li>

			<li>80 g de cacao</li>

			<li>1 oeuf</li>

			<li>450 g de crème liquide</li>

			<li>1 cuillère à café d'extrait de vanille</li>

		</ul>



		<ol>

			<li>Mélanger le lait, l'oeuf et le sucre dans une petite casserole.</li>

			<li>Faire chauffer le mélange à feu doux sans bouillir, tout en remuant jusqu’à ce qu’il commence à épaissir et prenne la consistance d’une crème anglaise légère.</li>

			<li>Versez ensuite le cacao tout en remuant jusqu’à ce que le mélange soit homogène.</li>

			<li>Laissez refroidir à température ambiante.</li>

			<li>Ajoutez en remuant la crème et la vanille.</li>

			<li>Mettez au réfrigérateur (minimum 12 heures).</li>

		</ol>

