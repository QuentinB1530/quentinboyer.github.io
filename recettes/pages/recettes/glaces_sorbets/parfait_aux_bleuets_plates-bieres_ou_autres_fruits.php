		<h3>Parfait aux Bleuets, Plates-bières ou autres fruits</h3>

		<ul>

			<li>500 g de fruits</li>

			<li>3 oeufs</li>

			<li>3 fois 70 g ou 3 fois 100 g de sucre</li>

			<li>1/4 L de crème fraîche</li>



			<p>Faire cuire les fruits avec 70 g de sucre. Passer au chinois ou mixer. Garder la moitié pour le coulis (l'autre moitié est réservé pour le parfait). Battre les 3 jaunes d'oeufs avec 70 g de sucre restant. Monter la crème fraîche en chantilly. Mélanger les jaunes d'oeufs, les blancs, la chantilly et la moitié des fruits en purée. Bien brasser et mettre dans un moule à charlotte en plastique ou garnir un moule à cake de papier ciré. Entreposer aussitôt au congélateur pour ne pas faire baisser les blancs d'oeufs. Démouler et servir avec le coulis.</p>

		



