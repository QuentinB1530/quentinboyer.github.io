		<h3>Couscous</h3>

		<p>Je n'ai pas de couscoussier seulemnt un faitout (je congèle en barquettes le reste)</p>



		<p>Viandes : collier d'agneau (1 morceau/personne), merguez, morceaux de poulet, 2 tranches de lard fumé</p>



		<p>Légumes : des petites courgettes, des carottes (en bottes ou de sable), des navets (taille balle ping pong), une petite boule de céleri avec branches, des tomates (2 à 3 pas plus), des oignons, une grosse boite de pois chiches, 2 à 3 poivrons rouges, semoule fine ( 1 verre à moutarde pour 2 personnes), des raisins secs que l'on fera tremper dans le bouillon chaud pour les réhydrater 1/4 d'heure avant de servir à part selon le goût de chacun.</p>



		<p>Epices : cumin, curcuma, paprika doux, muscade, cannelle, piment espelette, poivre : 1 cuillère à café de chaque</p>



		<p>Ras El Hanout, concentré tomates, coriandre (surgelée), ail émincée, sel, feuilles de laurier, thym : 1 cuillère à soupe de chaque</p>



		<p>1 tube d'Harissa (à délayer dans du bouillon) : servie à part selon les goûts (très pimentée 2 à 3 pois suffisent)</p>


		<p>Préparartion :</p>


		<ol>

			<li>Dorer la viande avec de l'huile d'olive, la réserver.</li>

			<li>Faire revenir un gros oignon dans la même huile avec le lard fumé.</li>

			<li>Préparer et couper les poivrons en lanière. Eplucher les carottes, les tailler en tronçons d'environ 5 cm en biseau, tailler les cougettes en laissant un peu de pelure (idem tronçons en biseau) les ranger à part car cuites en dernier, ajouter quelques branches de céleri. Bien les laver. Verser dans le faitout, ajouter toutes les épices sauf l'Harissa. Ajouter de l'eau et les légumes (sauf les courgettes) et toutes les viandes sauf merguez cuites à part. Il faut compter en fonction de la quantité de légumes 1 bonne heure, voire pus comme un pot au feu. Les carottes doivent rester un peu croquantes. 20 min avant la cuisson, ajouter les cougettes</li>

		</ol>



		<p>Pendant ce temps, préparer la semoule, version rapide : mélanger la semoule avec de l'huile d'olive (1 cuillère à soupe)  dans un plat creux, chauffer de l'eau (1 verre de semoule : 1 verre d'eau) verser sur la semoule et couvrir. Attendre 1/4 d'heure vant d'égrainer à la fourchette, la semoule. Réserver. Au moment de la servir, ajouter une noix de beurre, et réchauffer au micro-ondes.</p>



		<p>Cuire à la poêle, les merguez.</p>



		<p>Le couscous est prêt. Bol d'Harissa, raisins secs, semoule, seront servis dans des récipients séparés en fonction des foûts de chacun.</p>





