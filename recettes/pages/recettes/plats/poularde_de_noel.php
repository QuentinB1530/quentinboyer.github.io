		<h3>Poularde de Noël</h3>

		<p>Pour : 4 couverts / Préparation : 30 min / Cuisson : 40 min</p>



		<ul>

			<li>1 poularde ou 1 chapon</li>

			<li>200 g de graisse de canard</li>

			<li>1 douzaine de petits oignons</li>

			<li>1 douzaine de châtaignes ou marrons</li>

			<li>1 tranche de lard non fumé</li>

			<li>1 gousse d'ail</li>

			<li>1 bouillon de volaille maison ou 1 cube</li>

			<li>2 branches de thym</li>

			<li>2 branches de romarin</li>

			<li>1verre de vin blanc</li>

		</ul>



		<ol>

			<li>Préchauffer le four à 200°C.</li>

			<li>A l'aide d'un ciseau, découper la poularde en deux sur la longueur puis couper les pattes afin de ne garder que la partie charnue du pilon.</li>

			<li>Faire tiédir la graisse de canard dans une casserole afin qu'elle devienne liquide. La verser dans un plat pouvant recevoir les 2 moitiés de poularde.</li>

			<li>Assaisonner votre moitié de poularde puis déposer les côtés peau en bas dans le plat. Mettre sur le feu pour la faire dorer.</li>

			<li>Couper les oignons, les châtaignes et les lardons.</li>

			<li>Ajouter le thym, le romarin et l'ail avec la volaille puis enfourner 40 minutes en arrosant régulièrement.</li>

			<li>Dans une poêle, mettre une cuillère à soupe de graisse de canard et faire revenir les oignons, les lardons et les châtaignes. Une fois le tout coloré les retirer puis verser le bouillon. Laisser réduire.</li>

			<li>Dix minutes avant la fin de la cuisson de la poularde, retourner la pour faire croustiller la peau et mettre dans un plat à part au four votre garniture 5 minutes.</li>

			<li>Retirer la poularde et la graisse de canard. Déglacer (décoller les sucs) à l'aide de votre vin blanc dans le fond du plat.</li>

			<li>Ajouter le jus de cuisson de la poêle, 1 cuillère de bouillon de volaille.</li>

			<li>Déposer votre garniture avec la poularde, nappée de votre sauce.</li>

			<li>Servir chaud.</li>

		</ol>





