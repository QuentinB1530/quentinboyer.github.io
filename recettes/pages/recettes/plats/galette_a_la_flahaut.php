		<h3>Galette à la Flahaut</h3>

		<p>Pour 5 à 6 personnes</p>



		<p>Cuisson : 50 minutes</p>



		<ul>

			<li>1 bloc de pâte feuilleté surgelé</li>

			<li>375 g de pommes de terre</li>

			<li>1 petit pot de crème fraîche</li>

			<li>200 g de jambon fumé</li>

			<li>6 œufs</li>

			<li>225 g de gruyère râpé</li>

		</ul>



		<p>Mettez à cuire les pommes de terre 20 à 25 minutes l’eau bouillante salée.</p>



		<p>Ecrasez les pommes de terre dans une terrine et ajoutez-leur le contenu d’un petit pot de crème fraîche, les œufs, le gruyère râpé, et le jambon fumé détaillé en petits dés. Salez et poivrez. Remuez soigneusement ces ingrédients ensemble pour obtenir une préparation homogène.</p>



		<p>Etalez la pâte feuilleté et garnissez le plat.</p>



		<p>Piquez le fond à la fourchette et garnissez avec la préparation.</p>



		<p>Mettez à four moyen et laissez cuire environ 30 minutes jusqu’à ce que la galette gonfle et dore convenablement.</p>



		<p>Démoulez sur un plat de service et laissez tiédir avant de déguster.</p>





