		<h3>Escalope de Saumon à l'oseille</h3>

		

			<p>Pour 4 personnes</p>



			<p>Préparation : 40 minutes</p>



			<p>Cuisson : 10 minutes</p>

		<ul>

			<li>900 g de saumon frais</li>

			<li>10 cL de crème fraîche</li>

			<li>4 cL de Vermouth</li>

			<li>3 ou 8 cL de Sancerre</li>

			<li>10 cL de fumet de poisson</li>

			<li>80 ou 30 g d’oseille fraîche</li>

			<li>3 échalotes hachées</li>

			<li>40 g de beurre</li>

			<li>½ citron</li>

		</ul>


		<p>Faire cuire des escalopes de saumon.</p>



		<p>Préparer les feuilles d’oseille.</p>



		<p>Dans 1 casserole, mettre vin blanc, Vermouth, échalotes, fumet.</p>



		<p>Faire réduire ce mélange.</p>



		<p>Ajouter la crème fraîche.</p>



		<p>Laisser bouillir 1 min et mettre l’oseille coupé en 2 – 3 morceaux.</p>



		<p>Faire cuire 30 secondes à ébullition.</p>



		<p>Retirer du feu et ajouter le beurre en morceaux. Sel et poivre.</p>



		<p>Cuire les escalopes de saumon.</p>





