		<h3>Pâte Brisée</h3>

			<h4>Première Recette</h4>

				<ul>

					<li>125 g de beurre</li>

					<li>250 g de farine</li>

					<li>1 pincée de sel</li>

					<li>1/2 verre d'eau</li>

				</ul>

				<p>Ceci pour une tarte.</p>

				<p>Tout d'abord beurre et farine à pétrir, puis le sel et enfin l'eau.</p>

				<p>Faire une boule, mettre au réfrigérateur.</p>

			<h4>Seconde Recette</h4>

				<p>Pour un moule de diamètre 26 cm</p>

				<ul>

					<li>200 g de farine</li>

					<li>100 g de beurre</li>

					<li>1/2 cuillère à café de sel fin</li>

				</ul>

				<p>Mettre la farine, le sel et le beurre en très petits morceaux dans une jatte. Effriter du bout des doigts.</p>

				<p>Verser lentement 5 cL d'eau froide. Mélanger grossièrement les ingrédients sans pétrir.</p>

				<p>Former une boule. La pâte doit être souple mais pas molle. Laisser reposer 30 min environ.</p>

				<p>L'étaler sur le plan de travail fariné avec un rouleau également fariné. La plié en 4, la mettre dans un moule beurré. La déplié et l'étaler. Faire cuire 30 min dans le four Th. 7 (210°C). Pour tartes, tartelettes, tourtes, rissoles et chaussons.</p>

				<p>Astuce : pour une cuisson sans garniture, piquer le fond à la fourchette et le tapisser de papier sulfurisé garni de légumes secs. Le retirer à mi-cuisson.</p>

				<p>Variante : pour une tarte sucrée, ajouter 1 cuillère à soupe de sucre.</p>





