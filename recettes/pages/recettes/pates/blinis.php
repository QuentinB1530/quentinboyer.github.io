		<h3>Blinis</h3>

		

		<p>Pour 8 personnes :</p>



		<ul>

			<li>3 oeufs</li>

			<li>350 g de farine</li>

			<li>1 cuillère à café de sucre en poudre</li>

			<li>15 g de levure fraîche de boulanger</li>

			<li>25 cL de lait</li>

			<li>30 g de beurre fondu</li>

			<li>1 cuillère à café de sel</li>

			<li>Huile pour la poêle</li>

		</ul>

		

		<p>Délayer la levure, le sucre et le sel dans 5 cL de lait tiède. Dans une jatte, verser ce mélange, la farine et les jaunes d'oeufs. Mélanger avec une spatule en bois, puis verser lentement le reste de lait tiède et le beurre fondu. La pâte doit être semi-fluide et lisse. Couvrir d'un linge. Laisser lever 2 h à température ambiante.</p>



		<p>Juste avant l'emploi, battre les trois blancs d'oeufs en neige, les incorporer délicatement à la pâte. Graisser légèrement une petite poêle avec un chiffon huilé, puis la faire chauffer à feu vif.</p>



		<p>Verser une louche de pâte de façon à former une crêpe de 5 mm d'épaisseur. Faire dorer sur chaque face. Réserver au chaud dans une feuille d'aluminium. Huiler la poêle entre chaque blinis.</p>



		<p>Astuce : utiliser de préférence une poêle à blinis car elle a juste la bonne taille (diamètre 15 cm).</p>



		<p>Variante : possibilité de panacher farine de blé et de sarrasin.</p>



