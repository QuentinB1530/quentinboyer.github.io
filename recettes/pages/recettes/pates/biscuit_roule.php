		<h3>Biscuit Roulé</h3>

		

		<p>Pour 6 à 8 personnes :</p>



		<ul>

			<li>4 oeufs</li>

			<li>100 g de farine</li>

			<li>125 g de sucre en poudre</li>

			<li>1/2 cuillère à café de levure chimique</li>

			<li>50 g de beurre</li>

		</ul>



		<p>Dans une petite casserole, faire fondre le beurre, le réserver. Séparer les blancs des jaunes d'oeufs. Monter les blancs en neige. Travailler les jaunes et le sucre jusqu'à ce que le mélange blanchisse et double de volume.</p>



		<p>Ajouter un tiers de la farine puis le beurre fondu refroidi, la levure et un tiers des blancs. Incorporer alternativement la farine et les blancs restants, terminer par les blancs. La pâte doit être ferme.</p>



		<p>Etaler la pâte sur une tôle recouverte d'un papier sulfurisé beurré. Faire cuire 12 min dans le four préchauffé Th. 7/8 (230°C).</p>



		<p>Aussitôt cuit, faire glisser le biscuit avec son papier sur le plan de travail. Rouler ensemble papier et biscuit. Laisser refroidir. Dérouler. Fourrer de crème ou de confiture. Rouler à nouveau en décollant le papier. Couper net aux deux bouts le biscuit roulé.</p>



		<p>Variante : pour une bûche, recouvrir le biscuit de crème au beurre et faire des stries à la fourchette.</p>



