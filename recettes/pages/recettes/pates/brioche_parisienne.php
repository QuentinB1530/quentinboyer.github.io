		<h3>Brioche Parisienne</h3>

		<p>Pour 6 personnes :</p>



		<ul>

			<li>3 oeufs + 1 jaune d'oeuf</li>

			<li>250 g de farine</li>

			<li>20 g de sucre</li>

			<li>180 g de beurre</li>

			<li>10 g de levure de boulanger</li>

			<li>2 cuillère à soupe de lait</li>

			<li>1 cuillère à café de sel</li>

		</ul>



		<p>Diluer la levure émiettée dans la moitié du lait tièdi. Verser le sucre et le sel dans l'autre moitié. Disposer la farine en puits, verser au centre le lait et un oeuf. Incorporer la farine et les autres oeufs.</p>



		<p>Travailler la pâte jusqu'à ce qu'elle se détache du plan de travail. Incorporer le beurre ramolli par morceaux. Travailler le mélange 10 min puis former une boule. La poser dans une terrine, couvrir d'un torchon et laisser lever 3 h à température ambiante.</p>



		<p>Verser la pâte, qui doit avoir doublé de volume, sur le plan de travail fariné. L'écraser pour évacuer l'air. Puis remettre dans une terrine, couvrir d'un linge et mettre au réfrérateur 6 heures.</p>



		<p>Allumer le four Th. 7 (210°C). Pétrir légèrement la pâte et faconner les 2/3 en boule. Placer celle-ci dans un moule à côtes beurré, puis faire un creux au centre. Façonner le reste de pâte en forme de poire l'enfoncer dans le creux. Laisser lever jusqu'à ce que la pâte arrive au bord du moule, puis dorer à l'oeuf. Enfourner 10 min puis baisser le four au Th. 6 (180°C) et laisser cuire encore 25 min. Démouler.</p>



