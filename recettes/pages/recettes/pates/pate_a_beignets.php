		<h3>Pâte à Beignets</h3>

		

		<p>Pour 20 pièces :</p>



		<ul>

			<li>1 gros oeuf</li>

			<li>125 g de farine</li>

			<li>1 cuillère à soupe de sucre</li>

			<li>5 cL d'eau, de lait ou de bière</li>

			<li>1 cuillère à soupe d'huile</li>

			<li>1/2 cuillère à café de sel</li>

			<li>Huile pour friture</li>

		</ul>



		<p>Mettre la farine dans une terrine. Faire un puis au centre. Ajouter l'oeuf, le sel, le sucre et l'huile. Mélanger. Verser le liquide choisi tout en mélangeant. Laisser reposer 1 à 2 heures. Eplucher les fruits (demi-bananes, rondelles de pommes, tranches d'ananas, etc.) et les saupoudrer de sucre. Avant de préparer les beignets, vérifier l'épaisseur de la pâte qui doit enrober la cuillère. Si elle trop liquide, incorporer un blanc d'oeuf en neige.</p>



		<p>Piquer les fruits avec une fourchette, les enrober de pâte et les plonger dans un bain de friture chaud (175°C) en évitant qu'ils ne se touchent. Au bout de 2 min, les retourner avec une écumoire. Dès qu'ils sont bien dorés, les égoutter soigneusement sur du papier absorbant. Saupoudrer de sucre et servir chaud.</p>



		<p>Variante : en cas de beignets de légumes, ne pas ajouter de sucre dans la pâte.</p>



